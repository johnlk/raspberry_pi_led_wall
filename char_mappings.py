def get_mapping(string):
  global chars

  mapping = ['','','','','']

  string = string.upper()

  for char in string:
    if char in chars:
      char_mapping = chars[char]
    else:
      char_mapping = ['0','0','0','0','0']
    mapping = [
      mapping[0] + char_mapping[0] + '0',
      mapping[1] + char_mapping[1] + '0',
      mapping[2] + char_mapping[2] + '0',
      mapping[3] + char_mapping[3] + '0',
      mapping[4] + char_mapping[4] + '0'
    ]

  return mapping
